// Function to Open Navigation Bar
function openMe() {
	x = document.getElementById("click");
	x.className = "nav_bar opened";
}

// Function to Close Navigation Bar
function closeMe() {
	x = document.getElementById("click");
	x.className = "nav_bar closed";
}

